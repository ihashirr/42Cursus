int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	ct;

	ct = 0;
	while ((s1[ct] || s2[ct]) && ct < n)
	{
		if (!(s1[ct] == s2[ct]))
		{
			return (s1[ct] - s2[ct]);
		}
		ct++ ;
	}
	return (0);
}
